def is_leap(year):
  if year % 4 == 0:
    if year % 100 == 0:
      if year % 400 == 0:
        return True
      else:
        return False
    else:
      return True
  else:
    return False

def days_in_month(year_input,month_input_index):
    month_days = [31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31]
    if is_leap(year=year_input) == True:
        print("Leap year that has 366 days.")
    else:
        print("Not leap year.")
    return month_days[month_input_index - 1]

  
  
